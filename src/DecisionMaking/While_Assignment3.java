package src.DecisionMaking;

public class While_Assignment3 {

    //print the numbers which are divisible by 7 for the range of 150 to 200

    public static void main(String[] args) {



        int i= 150;

        while (i<=200){

            if (i%7==0){


                System.out.printf("Numbers which are divisible by 7 are %d \n",i);

            }

            i++;

        }


    }
}
