package src.DecisionMaking;

public class While_Assignment2 {

    //print even numbers between 200 and 500

    public static void main(String[] args) {


        int i=200;

        while (i<=500){

            if(i%2==0){

                System.out.printf("Even numbers are %d \n",i);

            }

            i++;

        }

    }
}
