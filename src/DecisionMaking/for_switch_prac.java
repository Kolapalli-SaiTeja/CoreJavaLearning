package src.DecisionMaking;

public class for_switch_prac {


    public static void main(String[] args) {

     int i;

       for(i=1;i<=20;i++){


           switch (i%2){

           case 0:

               System.out.println( i+ " is a Even number" );

               continue;

           case 1:
               System.out.println(i+ " is a ODD number");


       }}

        System.out.println(i);


    }
}
