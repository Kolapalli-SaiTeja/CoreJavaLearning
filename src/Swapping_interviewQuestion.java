package src;

public class Swapping_interviewQuestion {

    public static void main(String[] args) {



        int a = 10;
        int b = 12;




        a= a+b; // a = 10+12(22)

        b = a-b;  //b = 22-12 (10)

        a= a-b;   // a = 22-10 (12)


        System.out.println("value of a "+a);
        System.out.println("value of b "+b);

    }
}
